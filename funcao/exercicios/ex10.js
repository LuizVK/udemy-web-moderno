/*10)​Crie uma função que verifica se um número inteiro passado 
como parêmetro é divisível por 3 e retorne true ou false. */

divisivelPor3 = valor => valor % 3 == 0 ? true : false

console.log(divisivelPor3(3))
console.log(divisivelPor3(6))
console.log(divisivelPor3(7))
console.log(divisivelPor3(9))
console.log(divisivelPor3(76))



